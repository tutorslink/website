#!/usr/bin/env node

/**
 * Build script to inject Firebase configuration from environment variables
 * This script reads Firebase config from environment variables and injects them into firebase-config.js
 * 
 * Usage:
 *   node build-firebase-config.js
 * 
 * Environment variables required:
 *   FIREBASE_API_KEY
 *   FIREBASE_AUTH_DOMAIN
 *   FIREBASE_PROJECT_ID
 *   FIREBASE_STORAGE_BUCKET
 *   FIREBASE_MESSAGING_SENDER_ID
 *   FIREBASE_APP_ID
 */

const fs = require('fs');
const path = require('path');

// Read environment variables
const firebaseConfig = {
  apiKey: process.env.FIREBASE_API_KEY || 'YOUR_API_KEY',
  authDomain: process.env.FIREBASE_AUTH_DOMAIN || 'YOUR_PROJECT_ID.firebaseapp.com',
  projectId: process.env.FIREBASE_PROJECT_ID || 'YOUR_PROJECT_ID',
  storageBucket: process.env.FIREBASE_STORAGE_BUCKET || 'YOUR_PROJECT_ID.appspot.com',
  messagingSenderId: process.env.FIREBASE_MESSAGING_SENDER_ID || 'YOUR_SENDER_ID',
  appId: process.env.FIREBASE_APP_ID || 'YOUR_APP_ID'
};

// Check if all required environment variables are set
const allConfigured = Object.values(firebaseConfig).every(value => !value.startsWith('YOUR_'));

if (!allConfigured) {
  console.warn('⚠️  Warning: Firebase environment variables are not fully configured.');
  console.warn('   Using placeholder values in firebase-config.js');
  console.warn('   Set the following environment variables to enable Firebase Authentication:');
  console.warn('   - FIREBASE_API_KEY');
  console.warn('   - FIREBASE_AUTH_DOMAIN');
  console.warn('   - FIREBASE_PROJECT_ID');
  console.warn('   - FIREBASE_STORAGE_BUCKET');
  console.warn('   - FIREBASE_MESSAGING_SENDER_ID');
  console.warn('   - FIREBASE_APP_ID');
} else {
  console.log('✓ Firebase configuration loaded from environment variables');
}

// Generate the firebase-config.js content
const configContent = `// Firebase Configuration and Initialization
// Using Firebase Modular SDK (v10+)
// 
// This file is auto-generated by build-firebase-config.js
// Configuration values are injected from environment variables during build

import { initializeApp } from 'https://www.gstatic.com/firebasejs/10.7.1/firebase-app.js';
import { getAuth } from 'https://www.gstatic.com/firebasejs/10.7.1/firebase-auth.js';

// Firebase configuration
// In production, these values are injected from environment variables during build
// For local development, set these values directly or use the build script
const firebaseConfig = {
  apiKey: "${firebaseConfig.apiKey}",
  authDomain: "${firebaseConfig.authDomain}",
  projectId: "${firebaseConfig.projectId}",
  storageBucket: "${firebaseConfig.storageBucket}",
  messagingSenderId: "${firebaseConfig.messagingSenderId}",
  appId: "${firebaseConfig.appId}"
};

// Validate configuration - check all fields, not just API key
const isConfigured = !Object.values(firebaseConfig).some(value => 
  typeof value === 'string' && value.startsWith('YOUR_')
);

if (!isConfigured) {
  console.error('❌ Firebase is not configured!');
  console.error('   Authentication will not work until you configure Firebase.');
  console.error('   Please update firebase-config.js with your actual Firebase credentials.');
  console.error('   See FIREBASE_SETUP.md for step-by-step instructions.');
}

// Initialize Firebase
const app = initializeApp(firebaseConfig);

// Initialize Firebase Authentication and export
export const auth = getAuth(app);

// Export configuration status for debugging
export const isFirebaseConfigured = isConfigured;
`;

// Write the file
const outputPath = path.join(__dirname, 'firebase-config.js');
fs.writeFileSync(outputPath, configContent, 'utf8');

console.log(`✓ Generated firebase-config.js${allConfigured ? ' with environment variables' : ' with placeholders'}`);

if (!allConfigured) {
  console.log('\nTo configure Firebase:');
  console.log('1. Create a Firebase project at https://console.firebase.google.com/');
  console.log('2. Enable Email/Password authentication');
  console.log('3. Get your config from Project Settings > Your Apps');
  console.log('4. Set environment variables or update firebase-config.js directly');
  console.log('\nSee FIREBASE_SETUP.md for detailed instructions.');
}
